#include <bits/stdc++.h>
using namespace std ;

int main() {
	int T , N ;
	scanf("%d" , &T) ;
	while(T--) {
		scanf("%d" , &N) ;
		vector <int> height(N) ;
		vector <int> taller_than(N) ;
		for(int i = 0 ; i < N ; i++)
			scanf("%d" , &height[i]) ;
		for(int i = 0 ; i < N ; i++)
			scanf("%d" , &taller_than[i]) ;
		
	}
}